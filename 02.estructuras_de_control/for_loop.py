
# Lista de diferentes datos
datos = ["Melchor", "Ruiz", 18, True]

# For-each para imprimir todos los datos uno por uno
for dato in datos:
    print(dato)

# While para imprimir todos los datos uno por uno
c = 0
while c < len(datos):
    print(datos[c])
    c += 1

# for utilizando el tipo de dato range para imprimir un rango de numeros
for n in range(10, 20):
    print(n)