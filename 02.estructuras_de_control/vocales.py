
# Pedir una letra al usuario
vocal = input("Ingrese una letra: ")

# Ver si es a
if vocal == "a" or vocal == "A":
    print(f"{vocal} es vocal")
# Ver si es e
elif vocal == "e" or vocal == "E":
    print(f"{vocal} es vocal")
# Ver si es i
elif vocal == "i" or vocal == "I":
    print(f"{vocal} es vocal")
# Ver si es o
elif vocal == "o" or vocal == "O":
    print(f"{vocal} es vocal")
# Ver si es u
elif vocal == "u" or vocal == "U":
    print(f"{vocal} es vocal")
# Opcion por si no es vocal
else:
    print(f"{vocal} no es vocal")