# Mostrar mensaje
print("Cociente y residuo de dos numeros enteros")

# Entrar datos al programa
num1 = input("Ingrese el primer numero: ")
num2 = input("Ingrese el segundo numero numero: ")

# Realizar operaciones
num1 = int(num1)
num2 = int(num2)
cociente = num1 // num2
residuo = num1 % num2

# Mostrar salida
print(f"El cociente es: {cociente}\nEl residuo es: {residuo}")
